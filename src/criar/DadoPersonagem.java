package criar;

public class DadoPersonagem {

	private String nome;
	private int vida;
	private int ataquePersonagem;
	private int defesaPersonagem;
	private int totalDefesa;
	private int totalAtaque;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getVida() {
		return vida;
	}

	public void setVida(int vida) {
		this.vida = vida;
	}

	public int getAtaquePersonagem() {
		return ataquePersonagem;
	}

	public void setAtaquePersonagem(int ataquePersonagem) {
		this.ataquePersonagem = ataquePersonagem;
	}

	public int getDefesaPersonagem() {
		return defesaPersonagem;
	}

	public void setDefesaPersonagem(int defesaPersonagem) {
		this.defesaPersonagem = defesaPersonagem;
	}

	public int getTotalDefesa() {
		return totalDefesa;
	}

	public void setTotalDefesa(int totalDefesa) {
		this.totalDefesa = totalDefesa;
	}

	public int getTotalAtaque() {
		return totalAtaque;
	}

	public void setTotalAtaque(int totalAtaque) {
		this.totalAtaque = totalAtaque;
	}

}
